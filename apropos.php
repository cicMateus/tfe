<h2>A propos</h2>

<p>Le monde du bénévolat est vaste et confus. En
effet, il existe une multitude d’associations. Le
bénévole qui souhaite faire un choix, n’a pas la
tâche facile.</p>
<p>Voilà pourquoi Solidaction est née.</p>
<p>Cette plateforme a pour but de simplifier la démarche
du bénévolat, comblant ainsi un secteur
quelque peu négligé.</p>
<p>Si tu n'as jamais eu l'occasion de devenir bénévole pour une quelconque raison,
	cette plateforme est pour toi.</p>


<p>Inscris-toi et bienvenu au monde simplifié du bénévolat!</p>

